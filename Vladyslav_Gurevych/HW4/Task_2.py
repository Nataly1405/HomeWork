#You have the number 2 as a variable. Multiply it by 2 in two ways.
# Accordingly, you need to divide it in 2 different ways by 2.

digit = 2

print(f'multiply classic: {digit * 2}')
print(f'multiply by bite operator << : {digit << 1}')
print(f'division classic: {digit / 2}')
print(f'division by bite operator >> : {digit >> 1}')

